static inline int get_sin(uint _rad){
	
	_rad %= SINSIZE << 2;
	uchar phase = (_rad / SINSIZE) & 3;
	
	uchar _idx = _rad % SINSIZE;
	
	if(phase & 1)
		_idx = (SINSIZE-1) - _idx;
	
	int result = pgm_read_word(&SINTABLE[_idx]);
	
	if(phase > 1)
		result *= -1;
	
	return result;
	
}

